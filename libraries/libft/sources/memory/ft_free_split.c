#include "libft.h"

void	*free_split(char **s, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len && s[i])
	{
		free(s[i]);
		s[i] = NULL;
		i++;
	}
	free(s);
	s = NULL;
	return (NULL);
}
